    private void selectFolderAndLoadImages() {
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int result = chooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFolder = chooser.getSelectedFile();
            albumPath = selectedFolder.getAbsolutePath();
            jLabel2.setText(albumPath);
            File[] imageFiles = getImageFiles(selectedFolder);

            if (imageFiles != null && imageFiles.length > 0) {
                images = new BufferedImage[imageFiles.length];
                for (int i = 0; i < imageFiles.length; i++) {
                    try {
                        images[i] = ImageIO.read(imageFiles[i]);
                        consoleX.consoleLog("Loaded image: " + imageFiles[i].getAbsolutePath());

                    } catch (IOException ex) {
                        ex.printStackTrace();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "No images found in the selected folder.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }

    private void processImages() {
        if (images == null) {
            JOptionPane.showMessageDialog(this, "No images loaded. Please select a folder first.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Example processing: Print the size of each image
        for (BufferedImage image : images) {
            if (image != null) {
                consoleX.consoleLog("Processing image of size: " + image.getWidth() + "x" + image.getHeight());

                // Add additional image processing code here
            }
        }
        consoleX.consoleLog("Front is  :" + front);
        BufferedImage ogRez = imageFilter.resizeImage(images[front], jLabel3.getWidth(), jLabel3.getHeight());
        ImageIcon icn = new ImageIcon(ogRez);
        jLabel3.setText(null);
        jLabel3.setIcon(icn);
    }

    private File[] getImageFiles(File folder) {
        // Define image file extensions
        String[] imageExtensions = { "jpg", "jpeg"};

        // Create a filter for image files
        FilenameFilter imageFilter = (dir, name) -> {
            for (String ext : imageExtensions) {
                if (name.toLowerCase().endsWith("." + ext)) {
                    return true;
                }
            }
            return false;
        };

        // List image files in the selected folder
        return folder.listFiles(imageFilter);
    }

