    private void setupTimer() {

        timer = new Timer(2500, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                showNextImage();
            }
        });
        timer.setInitialDelay(0); // Start immediately
    }

    public void startSlideShow() {
        front = 0;
        setButtons(false);
        if (images != null && images.length > 0) {
            timer.start();
        }
    }

    public void stopSlideShow() {
        timer.stop();
        setButtons(true);
        consoleX.consoleLog("Slide Show : End");
    }

    private void showNextImage() {
        if (images != null && images.length > 0) {
            jLabel4.setText("MODE : Slide Show ");
            BufferedImage ogRez = imageFilter.resizeImage(images[front], jLabel3.getWidth(), jLabel3.getHeight());
            ImageIcon icon = new ImageIcon(ogRez);
            jLabel3.setIcon(icon);

            
            front = (front + 1) % images.length;
        }
    }

